# Tuples

my_tuples = (1, 2, 3, 4, 5)
print(my_tuples)
print(my_tuples[0])  # Accessing the first element
print(my_tuples[2])  # Accessing the third element
print(my_tuples[-1])  # Accessing the last element


tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)

# Concatenating tuples
conc_tuple = tuple1 + tuple2
print(conc_tuple)  # Output: (1, 2, 3, 4, 5, 6)
rep_tuple = tuple1 * 3
print(rep_tuple)  # Output: (1, 2, 3, 1, 2, 3, 1, 2, 3)